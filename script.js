let currentPresetIndex = 0;

function loadPreset(index) {
    const preset = presets[index];
    // UPDATED: Targets the h1 on the test screen by its specific ID
    const title = document.getElementById('preset-title');
    const audioPlayer = document.getElementById('audio-player');
    const audioSource = document.getElementById('audio-source');
    const questionsContainer = document.getElementById('questions-container');

    title.textContent = `Preset ${index + 1} of ${presets.length}`;
    audioSource.src = `audio/${preset.fileName}`;
    audioPlayer.load();
    questionsContainer.innerHTML = '';

    function createVotingScale(category, tag) {
        const groupName = `${category}-${tag.replace(/\s+/g, '-')}`;
        return `
            <div class="voting-scale">
                1 <input type="radio" name="${groupName}" value="1">
                2 <input type="radio" name="${groupName}" value="2">
                3 <input type="radio" name="${groupName}" value="3">
                4 <input type="radio" name="${groupName}" value="4">
                5 <input type="radio" name="${groupName}" value="5">
            </div>
        `;
    }

    preset.cyaniteTags.advancedInstrument.forEach(tag => {
        questionsContainer.innerHTML += `
            <div class="question">
                <p>Instrument: <strong>${tag}</strong>. How well do you agree?</p>
                ${createVotingScale('instrument', tag)}
            </div>
        `;
    });

    preset.cyaniteTags.genre.forEach(tag => {
        questionsContainer.innerHTML += `
            <div class="question">
                <p>Genre: <strong>${tag}</strong>. How well do you agree?</p>
                ${createVotingScale('genre', tag)}
            </div>
        `;
    });
    
    preset.cyaniteTags.subgenre.forEach(tag => {
        if (tag !== "N/A") {
            questionsContainer.innerHTML += `
                <div class="question">
                    <p>Subgenre: <strong>${tag}</strong>. How well do you agree?</p>
                    ${createVotingScale('subgenre', tag)}
                </div>
            `;
        }
    });

    preset.cyaniteTags.advancedMood.forEach(tag => {
        questionsContainer.innerHTML += `
            <div class="question">
                <p>Mood: <strong>${tag}</strong>. How well do you agree?</p>
                ${createVotingScale('advancedMood', tag)}
            </div>
        `;
    });

    preset.cyaniteTags.character.forEach(tag => {
        questionsContainer.innerHTML += `
            <div class="question">
                <p>Character: <strong>${tag}</strong>. How well do you agree?</p>
                ${createVotingScale('character', tag)}
            </div>
        `;
    });
}

// --- NEW: Main script execution ---
// This code runs after the page has loaded.
document.addEventListener('DOMContentLoaded', function() {
    const startBtn = document.getElementById('start-btn');
    const welcomeContainer = document.getElementById('welcome-container');
    const testContainer = document.getElementById('test-container');

    // Add a click listener to the 'Continue' button
    startBtn.addEventListener('click', function() {
        // Hide the welcome screen
        welcomeContainer.style.display = 'none';
        // Show the test screen
        testContainer.style.display = 'block';
        // Load the first preset
        loadPreset(currentPresetIndex);
    });
});